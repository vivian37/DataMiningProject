let currData = [];
let currStockId = 'Stock';
let myChart;
let upColor = '#ec0000';
let upBorderColor = '#8A0000';
let downColor = '#00da3c';
let downBorderColor = '#008F28';
let stockList = [];
$(document).ready(function () {
    let dom = document.getElementById("ec-container");
    myChart = echarts.init(dom);
    $.ajax({
        url: '/data/stockList',
        success: function (d) {
            stockList = d;

        }
    });
    $('#showBtn').click(function () {
        let stock_id = $('#stockIdInput').val();
        if (stockList.indexOf(stock_id) !== -1) {
            $.ajax({
                url: '/data/stock/day/' + stock_id,
                success: function (d) {
                    currData = splitData(d);
                    currStockId = stock_id;
                    draw();
                }
            })
        } else {
            alert('Stock ' + stock_id + ' not in database!');
        }
    });
    draw();
});

function splitData(rawData) {
    var categoryData = [];
    var values = [];
    for (var i = 0; i < rawData.length; i++) {
        categoryData.push(rawData[i].splice(0, 1)[0]);
        values.push(rawData[i])
    }
    return {
        categoryData: categoryData,
        values: values
    };
}

function calculateMA(dayCount) {
    var result = [];
    for (var i = 0, len = currData.values.length; i < len; i++) {
        if (i < dayCount) {
            result.push('-');
            continue;
        }
        var sum = 0;
        for (var j = 0; j < dayCount; j++) {
            sum += currData.values[i - j][1];
        }
        result.push(sum / dayCount);
    }
    return result;
}

